// Creo un array de números para practicar la lógica
let Fandiño3 = [2, 5, 8, 11, 14, 7];

// Variable para contar cuántos son pares
let David4 = 0;

// Recorro el array con un for clásico porque quiero control total del índice
for (let Fabian7 = 0; Fabian7 < Fandiño3.length; Fabian7++) {
    if (Fandiño3[Fabian7] % 2 === 0) {
        David4++; // Aquí aumento el contador si el número es par
    }
}

// Imprimo cuántos pares encontré
console.log(David4);
