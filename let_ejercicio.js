// Declaración con let
let fabian = 20;
console.log("Edad inicial:", fabian);

// Se puede cambiar (reasignar)
fabian = 25;
console.log("Edad modificada:", fabian);

// Declaración con const
const david = "Colombia";
console.log("País:", david);

// Intentar cambiarlo genera ERROR
david = "México"; // generara error al intentar cambiarlo
