// Inicializo el array que quiero recorrer
let Torres2 = [3, 7, 10, 5];

// Declaro una variable para acumular la suma
let Fandiño2 = 0;

// Recorro el array y voy sumando los valores uno por uno
for (let Fabian5 = 0; Fabian5 < Torres2.length; Fabian5++) {
    Fandiño2 += Torres2[Fabian5];
}

// Muestro la suma total
console.log(Fandiño2);
